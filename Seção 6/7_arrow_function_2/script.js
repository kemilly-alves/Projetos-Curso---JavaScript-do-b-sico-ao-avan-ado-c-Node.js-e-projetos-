const multiplicarPorDois = (x) => {
	return x * 2;
}

console.log(multiplicarPorDois(2)); // 4


const multiplicarPorDois2 = x => x * 2;

console.log(multiplicarPorDois2(4)); // 8

// Quando a expressão for simples e tiver só um parâmetro, a gente pode fazer desse jeito exemplo "const multiplicarPorDois2 = x => x * 2;" arrow function.
// Mas sempre prezem pela leitura do código, se ficar ruim de ler ou interpretar não utilizem essa abordagem, utilizem a abordagem normal, pois isso é melhor do que fazer coisas que vão gerar retrabalho ou até muito tempo no entenidmento do código há um outro programador. 
