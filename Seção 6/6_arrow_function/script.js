let consoleTeste = () => {
	console.log("Olá mundo!");
}

consoleTeste(); // Olá mundo! - Ele esta chamando a função consoleTeste, que é uma arrow function, e ela imprime "Olá mundo!" no console


let soma = (a,b) => {
	return a + b;
};


console.log(soma(10,20)); // 30 - Ele esta chamando a função soma, que é uma arrow function, e ela retorna a soma dos dois parâmetros passados
