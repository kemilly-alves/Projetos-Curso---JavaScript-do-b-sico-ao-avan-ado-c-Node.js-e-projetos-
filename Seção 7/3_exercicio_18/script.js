function somar (a,b) {
    return a + b;
}

console.log(somar(40,10)); // 50 - Ele esta chamando a função soma, passando os argumentos 40 e 10, e como a função espera dois argumentos, ela retorna a soma dos dois argumentos, que é 50

console.log(somar(20,50)); // 70 - Ele esta chamando a função soma, passando os argumentos 20 e 50, e como a função espera dois argumentos, ela retorna a soma dos dois argumentos, que é 70  