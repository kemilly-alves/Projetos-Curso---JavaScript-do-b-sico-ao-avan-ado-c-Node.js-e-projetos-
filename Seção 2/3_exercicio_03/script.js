// Maior que (>) 
console.log(5 > 3);  //true
console.log(2 > 9);  //false
// Menor que (<) 
console.log(9 < 15);  //true
console.log(10 < 3);  //false
// Maior ou igual que (>=) 
console.log(5 >= 3);  //true
console.log(2 >= 9);  //false
// Menor ou igual que (<=) 
console.log(9 <= 15);  //true
console.log(10 <= 3);  //false
// Iugal a (==)
console.log(5 == 5);  //true
console.log(2 == 9);  //false
// Diferente de (!=)
console.log(5 != 5);  //false
console.log(2 != 9);  //true
// Identifico a (===)
console.log(5 === 5);  //true
console.log(2 === 9);  //false
// Não identico a (!==)
console.log(5 !== 5);  //false
console.log(2 !== 9);  //true



